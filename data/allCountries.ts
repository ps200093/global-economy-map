export function getCountryByISO3(iso3: string): CountryBasicInfo | undefined {
  return ALL_COUNTRIES.find(c => c.iso3 === iso3);
}

/**
 * 지역별 국가 목록
 */
export function getCountriesByRegion(region: string): CountryBasicInfo[] {
  return ALL_COUNTRIES.filter(c => c.region === region);
}

